package ArrayListProcess;

public class Divide {
	double x;
	double  y;
	
	public Divide() {

		x = 0;
		y = 0;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
	public double doDivide() {
		
		return  x / y ;
	}
	
}